package de.tgl.anthology.pojos;

public class FilePojo {
    private String readFromDirectory;
    private String writeToDirectory;

    public String getReadFromDirectory() {
        return readFromDirectory;
    }

    public void setReadFromDirectory(String readFromDirectory) {
        this.readFromDirectory = readFromDirectory;
    }

    public String getWriteToDirectory() {
        return writeToDirectory;
    }

    public void setWriteToDirectory(String writeToDirectory) {
        this.writeToDirectory = writeToDirectory;
    }
}
